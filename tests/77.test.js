import { combine } from '../src/77.js';
import { arrToStr } from './util.js';

describe('77. Combinations', () => {
  [
    [
      4,
      2,
      [
        [1, 2],
        [1, 3],
        [1, 4],
        [2, 3],
        [2, 4],
        [3, 4],
      ],
    ],
    [5, 5, [[1, 2, 3, 4, 5]]],
    [
      5,
      4,
      [
        [1, 2, 3, 4],
        [1, 2, 3, 5],
        [1, 2, 4, 5],
        [1, 3, 4, 5],
        [2, 3, 4, 5],
      ],
    ],
    [
      5,
      3,
      [
        [1, 2, 3],
        [1, 2, 4],
        [1, 2, 5],
        [1, 3, 4],
        [1, 3, 5],
        [1, 4, 5],
        [2, 3, 4],
        [2, 3, 5],
        [2, 4, 5],
        [3, 4, 5],
      ],
      [
        5,
        2,
        [
          [1, 2],
          [1, 3],
          [1, 4],
          [1, 5],
          [2, 3],
          [2, 4],
          [2, 5],
          [3, 4],
          [3, 5],
          [4, 5],
        ],
      ],
      [5, 1, [[1], [2], [3], [4], [5]]],
    ],
  ].forEach(([a, b, expected]) => {
    test(`${a},${b} -> ${arrToStr(expected)}`, () => {
      const result = combine(a, b);
      expect(result).toEqual(expected);
    });
  });
});
